package punt.punt1;

public class Punt
{
    //Attributes
    private int x;
    private int y;

    //Constructor
    public Punt(int x, int y)
    {
        this.x = x;
        this.y = y;
    }

    //Getters
    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
